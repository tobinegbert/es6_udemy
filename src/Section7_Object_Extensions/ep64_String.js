/** String.startsWith, endsWith, and includes function **/
let name = "Tobin";

    //Checks to see if the string starts with certain characters, it's also case sensitive
console.log(name.startsWith("To"));
    //Checks to see if the string ends with certain characters
console.log(name.endsWith("bin"));
    //Checks to see if the string includes certain characters
console.log(name.includes("obi"));
    //Allows you to check without writing regex